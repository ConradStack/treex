sub process_tnode {
	my ( $self, $t_node ) = @_;

    my $src_tnode = $t_node->src_tnode;

    if ($src_tnode) {

        my $src_parent = $src_tnode->get_parent;

        my $key = $src_parent->t_lemma." ".$src_tnode->formeme;
        my $parent = $t_node->get_parent;

        if ($CS2RU_FORMEME{$key}) {
            my ($ru_t_lemma, $ru_formeme) = split / /, $CS2RU_FORMEME{$key};
            $t_node->set_formeme($ru_formeme);
            $parent->set_t_lemma($ru_t_lemma);
        }
        # TODO: make sure it is a verb node
    }
    

}

